function shuffleArray<T>(array: T[]): T[] {
  const shuffled = [...array]
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1))
    ;[shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]]
  }
  return shuffled
}

export function generateLocalPoem(
  theme: string,
  poemLength: number,
  poeticDevices: string[],
  customPrompt?: string,
): string {
  const themeKey = (theme as keyof typeof poemLines) || "nature"
  const lines = poemLines[themeKey] || poemLines.nature

  const selectedLines: string[] = []

  // Add opening lines
  const openings = shuffleArray(lines.opening)
  selectedLines.push(openings[0])
  if (poemLength > 8) selectedLines.push(openings[1])

  // Add middle lines based on poem length
  const middles = shuffleArray(lines.middle)
  const middleCount = Math.min(Math.floor(poemLength / 3), middles.length)
  for (let i = 0; i < middleCount; i++) {
    selectedLines.push(middles[i])
  }

  // Add poetic device lines if selected
  if (poeticDevices.length > 0) {
    for (const device of poeticDevices.slice(0, 2)) {
      const deviceLines = poeticDeviceEnhancements[device]
      if (deviceLines) {
        const randomLine = deviceLines[Math.floor(Math.random() * deviceLines.length)]
        selectedLines.push(randomLine)
      }
    }
  }

  // Add more middle lines if needed
  const remainingMiddles = shuffleArray(middles.slice(middleCount))
  while (selectedLines.length < poemLength - 2 && remainingMiddles.length > 0) {
    selectedLines.push(remainingMiddles.pop()!)
  }

  // Add closing lines
  const closings = shuffleArray(lines.closing)
  selectedLines.push(closings[0])
  if (poemLength > 10) selectedLines.push(closings[1])

  // Add custom prompt inspired line if provided
  if (customPrompt) {
    const customInspiredLines = [
      `${customPrompt.split(" ").slice(0, 3).join(" ")} whispers through the dream,`,
      `Of ${customPrompt.split(" ").slice(0, 2).join(" ")}, the heart does softly speak,`,
    ]
    selectedLines.splice(
      Math.floor(selectedLines.length / 2),
      0,
      customInspiredLines[Math.floor(Math.random() * customInspiredLines.length)],
    )
  }

  // Trim to desired length
  const finalLines = selectedLines.slice(0, poemLength)

  return finalLines.join("\n")
}
